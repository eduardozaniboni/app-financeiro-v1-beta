import React, { useState, useRef, useEffect } from 'react';
import { useFinanceStore } from '@/store/useFinanceStore';
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card';
import { Input } from '@/components/ui/input';
import { Button } from '@/components/ui/button';
import { Badge } from '@/components/ui/badge';
import { ScrollArea } from '@/components/ui/scroll-area';
import { 
  ChatBubbleLeftRightIcon,
  PaperAirplaneIcon,
  MicrophoneIcon,
  PhotoIcon,
  CurrencyDollarIcon,
  ChartBarIcon,
  CalendarIcon,
  UserIcon,
  CpuChipIcon
} from '@heroicons/react/24/outline';
import toast from 'react-hot-toast';

interface Message {
  id: string;
  type: 'user' | 'ai';
  content: string;
  timestamp: Date;
  category?: 'transaction' | 'query' | 'suggestion' | 'analysis';
}

export default function Chatbot() {
  const { 
    transactions, 
    addTransaction, 
    getTotalBalance, 
    getMonthlyIncome, 
    getMonthlyExpenses,
    goals,
    assets,
    getAssetsValue
  } = useFinanceStore();
  
  const [messages, setMessages] = useState<Message[]>([
    {
      id: '1',
      type: 'ai',
      content: 'üëã Ol√°! Sou seu assistente financeiro inteligente. Posso ajudar voc√™ a:\n\n‚Ä¢ Registrar gastos e receitas\n‚Ä¢ Consultar seu saldo e resumos\n‚Ä¢ Analisar seus h√°bitos financeiros\n‚Ä¢ Dar sugest√µes de economia\n\nComo posso ajudar hoje?',
      timestamp: new Date(),
      category: 'suggestion'
    }
  ]);
  
  const [inputMessage, setInputMessage] = useState('');
  const [isProcessing, setIsProcessing] = useState(false);
  const messagesEndRef = useRef<HTMLDivElement>(null);

  const scrollToBottom = () => {
    messagesEndRef.current?.scrollIntoView({ behavior: 'smooth' });
  };

  useEffect(() => {
    scrollToBottom();
  }, [messages]);

  const processMessage = async (message: string) => {
    setIsProcessing(true);
    
    // Simula√ß√£o de processamento de IA
    await new Promise(resolve => setTimeout(resolve, 1000));
    
    const response = generateAIResponse(message.toLowerCase());
    
    const aiMessage: Message = {
      id: Date.now().toString() + '_ai',
      type: 'ai',
      content: response.content,
      timestamp: new Date(),
      category: response.category
    };
    
    setMessages(prev => [...prev, aiMessage]);
    setIsProcessing(false);
  };

  const generateAIResponse = (message: string): { content: string; category: Message['category'] } => {
    // Padr√µes para detectar transa√ß√µes
    const expensePatterns = [
      /gastei (r\$\s?)?(\d+(?:,\d{2})?)\s*(.*)/i,
      /paguei (r\$\s?)?(\d+(?:,\d{2})?)\s*(.*)/i,
      /comprei\s*(.*?)\s*por\s*(r\$\s?)?(\d+(?:,\d{2})?)/i,
      /despesa\s*de\s*(r\$\s?)?(\d+(?:,\d{2})?)\s*(.*)/i
    ];

    const incomePatterns = [
      /recebi (r\$\s?)?(\d+(?:,\d{2})?)\s*(.*)/i,
      /ganhei (r\$\s?)?(\d+(?:,\d{2})?)\s*(.*)/i,
      /renda\s*de\s*(r\$\s?)?(\d+(?:,\d{2})?)\s*(.*)/i
    ];

    // Detectar transa√ß√µes de gastos
    for (const pattern of expensePatterns) {
      const match = message.match(pattern);
      if (match) {
        const amount = parseFloat(match[2].replace(',', '.'));
        const description = match[3] || match[1] || 'Gasto registrado';
        
        addTransaction({
          type: 'expense',
          amount,
          description: description.trim(),
          category: categorizeExpense(description),
          date: new Date().toISOString().split('T')[0]
        });

        return {
          content: `‚úÖ Gasto registrado com sucesso!\n\nüí∞ Valor: ${amount.toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' })}\nüìù Descri√ß√£o: ${description}\nüìÅ Categoria: ${categorizeExpense(description)}\n\nSeu saldo atual √© ${getTotalBalance().toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' })}`,
          category: 'transaction'
        };
      }
    }

    // Detectar transa√ß√µes de receitas
    for (const pattern of incomePatterns) {
      const match = message.match(pattern);
      if (match) {
        const amount = parseFloat(match[2].replace(',', '.'));
        const description = match[3] || match[1] || 'Receita registrada';
        
        addTransaction({
          type: 'income',
          amount,
          description: description.trim(),
          category: 'Receita',
          date: new Date().toISOString().split('T')[0]
        });

        return {
          content: `‚úÖ Receita registrada com sucesso!\n\nüí∞ Valor: ${amount.toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' })}\nüìù Descri√ß√£o: ${description}\n\nSeu saldo atual √© ${getTotalBalance().toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' })}`,
          category: 'transaction'
        };
      }
    }

    // Consultas sobre saldo e resumos
    if (message.includes('saldo') || message.includes('quanto tenho')) {
      const balance = getTotalBalance();
      const monthlyIncome = getMonthlyIncome();
      const monthlyExpenses = getMonthlyExpenses();
      
      return {
        content: `üí∞ **Resumo Financeiro Atual**\n\n‚Ä¢ **Saldo total:** ${balance.toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' })}\n‚Ä¢ **Receitas este m√™s:** ${monthlyIncome.toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' })}\n‚Ä¢ **Gastos este m√™s:** ${monthlyExpenses.toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' })}\n‚Ä¢ **Sobra mensal:** ${(monthlyIncome - monthlyExpenses).toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' })}`,
        category: 'query'
      };
    }

    if (message.includes('resumo') || message.includes('relat√≥rio')) {
      const totalTransactions = transactions.length;
      const totalAssets = getAssetsValue();
      const totalGoals = goals.length;
      
      return {
        content: `üìä **Resumo Completo**\n\n**Finan√ßas:**\n‚Ä¢ ${totalTransactions} transa√ß√µes registradas\n‚Ä¢ Saldo: ${getTotalBalance().toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' })}\n‚Ä¢ Patrim√¥nio em ativos: ${totalAssets.toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' })}\n\n**Planejamento:**\n‚Ä¢ ${totalGoals} metas ativas\n‚Ä¢ ${goals.filter(g => (g.currentAmount / g.targetAmount) >= 1).length} metas conclu√≠das\n\n**Tend√™ncia:** ${getMonthlyIncome() > getMonthlyExpenses() ? 'üìà Positiva' : 'üìâ Aten√ß√£o aos gastos'}`,
        category: 'analysis'
      };
    }

    if (message.includes('gastos') || message.includes('despesas')) {
      const monthlyExpenses = getMonthlyExpenses();
      const expensesByCategory = transactions
        .filter(t => t.type === 'expense' && t.date.startsWith(new Date().toISOString().substring(0, 7)))
        .reduce((acc, t) => {
          acc[t.category] = (acc[t.category] || 0) + t.amount;
          return acc;
        }, {} as Record<string, number>);

      const categoryList = Object.entries(expensesByCategory)
        .sort(([,a], [,b]) => b - a)
        .slice(0, 5)
        .map(([category, amount]) => `‚Ä¢ ${category}: ${amount.toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' })}`)
        .join('\n');

      return {
        content: `üí∏ **Gastos deste m√™s: ${monthlyExpenses.toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' })}**\n\n**Top categorias:**\n${categoryList}\n\nüí° **Dica:** Monitore as categorias com maiores gastos para identificar oportunidades de economia!`,
        category: 'analysis'
      };
    }

    if (message.includes('dica') || message.includes('sugest√£o') || message.includes('economia')) {
      const suggestions = [
        'üí° **Dica de Economia:** Use a regra 50-30-20: 50% para necessidades, 30% desejos, 20% poupan√ßa.',
        'üéØ **Estrat√©gia:** Defina metas espec√≠ficas para seus objetivos financeiros - isso aumenta em 60% as chances de sucesso!',
        'üìä **An√°lise:** Revise seus gastos mensalmente e identifique padr√µes. Pequenos ajustes podem gerar grandes economias.',
        'üîÑ **H√°bito:** Automatize suas poupan√ßas - configure transfer√™ncias autom√°ticas para sua reserva de emerg√™ncia.',
        'üì± **Tecnologia:** Use apps como este para registrar gastos em tempo real. O controle visual melhora muito a gest√£o!',
        'üí∞ **Investimento:** Antes de comprar algo, pergunte: "Este valor investido poderia me render mais no futuro?"'
      ];
      
      const randomSuggestion = suggestions[Math.floor(Math.random() * suggestions.length)];
      return {
        content: randomSuggestion,
        category: 'suggestion'
      };
    }

    if (message.includes('metas') || message.includes('objetivos')) {
      const activeGoals = goals.length;
      const completedGoals = goals.filter(g => (g.currentAmount / g.targetAmount) >= 1).length;
      const totalGoalsValue = goals.reduce((sum, g) => sum + g.targetAmount, 0);
      
      return {
        content: `üéØ **Suas Metas Financeiras**\n\n‚Ä¢ **Total de metas:** ${activeGoals}\n‚Ä¢ **Metas conclu√≠das:** ${completedGoals}\n‚Ä¢ **Valor total das metas:** ${totalGoalsValue.toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' })}\n\n${activeGoals > 0 ? '‚ú® Continue firme! Metas claras s√£o o primeiro passo para o sucesso financeiro.' : 'üí° Que tal definir sua primeira meta? Comece com algo alcan√ß√°vel em 6 meses!'}`,
        category: 'analysis'
      };
    }

    // Resposta padr√£o inteligente
    const defaultResponses = [
      'ü§î N√£o entendi completamente sua mensagem. Posso ajudar voc√™ a:\n\n‚Ä¢ Registrar gastos (Ex: "Gastei R$ 50 no supermercado")\n‚Ä¢ Consultar saldo ("Qual meu saldo?")\n‚Ä¢ Ver resumos ("Mostrar resumo")\n‚Ä¢ Dicas de economia ("D√™ uma dica")\n\nTente reformular sua pergunta!',
      'üí≠ Hmm, n√£o consegui processar isso. Algumas sugest√µes:\n\n‚Ä¢ "Paguei R$ 100 na farm√°cia"\n‚Ä¢ "Recebi R$ 500 de freelance"\n‚Ä¢ "Como est√£o meus gastos?"\n‚Ä¢ "Preciso de uma sugest√£o"\n\nO que voc√™ gostaria de fazer?',
      'üîç N√£o identifiquei um comando espec√≠fico. Posso te ajudar com:\n\n‚úÖ Registrar transa√ß√µes\n‚úÖ Consultar informa√ß√µes financeiras\n‚úÖ An√°lises e relat√≥rios\n‚úÖ Dicas personalizadas\n\nComo posso ser √∫til?'
    ];

    return {
      content: defaultResponses[Math.floor(Math.random() * defaultResponses.length)],
      category: 'suggestion'
    };
  };

  const categorizeExpense = (description: string): string => {
    const categories = {
      'Alimenta√ß√£o': ['supermercado', 'restaurante', 'lanche', 'comida', 'almo√ßo', 'jantar', 'caf√©'],
      'Transporte': ['uber', 'taxi', '√¥nibus', 'metro', 'gasolina', 'combust√≠vel', 'estacionamento'],
      'Moradia': ['aluguel', 'luz', '√°gua', 'g√°s', 'internet', 'condom√≠nio'],
      'Sa√∫de': ['farm√°cia', 'm√©dico', 'consulta', 'exame', 'dentista', 'hospital'],
      'Educa√ß√£o': ['curso', 'livro', 'escola', 'faculdade', 'material'],
      'Lazer': ['cinema', 'teatro', 'show', 'viagem', 'parque', 'divers√£o'],
      'Compras': ['roupa', 'sapato', 'presente', 'eletr√¥nico', 'casa'],
      'Tecnologia': ['celular', 'computador', 'software', 'app', 'streaming']
    };

    for (const [category, keywords] of Object.entries(categories)) {
      if (keywords.some(keyword => description.toLowerCase().includes(keyword))) {
        return category;
      }
    }

    return 'Outros';
  };

  const handleSendMessage = async () => {
    if (!inputMessage.trim()) return;

    const userMessage: Message = {
      id: Date.now().toString(),
      type: 'user',
      content: inputMessage,
      timestamp: new Date(),
    };

    setMessages(prev => [...prev, userMessage]);
    const messageToProcess = inputMessage;
    setInputMessage('');

    await processMessage(messageToProcess);
  };

  const handleKeyPress = (e: React.KeyboardEvent) => {
    if (e.key === 'Enter' && !e.shiftKey) {
      e.preventDefault();
      handleSendMessage();
    }
  };

  const simulateVoiceInput = () => {
    const voiceExamples = [
      'Gastei R$ 45 no posto de gasolina hoje',
      'Paguei R$ 120 no supermercado',
      'Recebi R$ 800 de freelance',
      'Qual meu saldo atual?',
      'Como est√£o meus gastos este m√™s?'
    ];
    
    const randomExample = voiceExamples[Math.floor(Math.random() * voiceExamples.length)];
    setInputMessage(randomExample);
    toast.success('üé§ √Åudio transcrito com sucesso!');
  };

  const simulateImageInput = () => {
    const imageExamples = [
      'Gastei R$ 89,90 no Supermercado Extra',
      'Paguei R$ 156,78 na farm√°cia Drogasil',
      'Almo√ßo no restaurante - R$ 34,50',
      'Uber para o aeroporto - R$ 67,20'
    ];
    
    const randomExample = imageExamples[Math.floor(Math.random() * imageExamples.length)];
    setInputMessage(randomExample);
    toast.success('üì∑ Imagem processada e texto extra√≠do!');
  };

  const quickCommands = [
    { text: 'Ver meu saldo', icon: CurrencyDollarIcon },
    { text: 'Resumo financeiro', icon: ChartBarIcon },
    { text: 'Gastos do m√™s', icon: CalendarIcon },
    { text: 'D√™ uma dica', icon: CpuChipIcon }
  ];

  return (
    <div className="h-[calc(100vh-120px)] flex flex-col space-y-4">
      {/* Header */}
      <div>
        <h1 className="text-3xl font-bold text-foreground mb-2">Assistente Financeiro IA</h1>
        <p className="text-muted-foreground">
          Converse naturalmente para controlar suas finan√ßas
        </p>
      </div>

      {/* Chat Container */}
      <Card className="flex-1 flex flex-col shadow-soft">
        <CardHeader className="pb-4">
          <CardTitle className="flex items-center gap-2">
            <ChatBubbleLeftRightIcon className="h-5 w-5 text-primary" />
            Chat Financeiro
            <Badge variant="secondary" className="ml-auto">
              Online
            </Badge>
          </CardTitle>
        </CardHeader>

        <CardContent className="flex-1 flex flex-col p-0">
          {/* Messages Area */}
          <ScrollArea className="flex-1 px-6">
            <div className="space-y-4 pb-4">
              {messages.map((message) => (
                <div
                  key={message.id}
                  className={`flex ${message.type === 'user' ? 'justify-end' : 'justify-start'}`}
                >
                  <div
                    className={`max-w-[80%] p-4 rounded-lg ${
                      message.type === 'user'
                        ? 'bg-primary text-primary-foreground'
                        : 'bg-accent text-accent-foreground'
                    }`}
                  >
                    <div className="flex items-start gap-2">
                      {message.type === 'ai' && (
                        <CpuChipIcon className="h-4 w-4 mt-0.5 text-primary flex-shrink-0" />
                      )}
                      {message.type === 'user' && (
                        <UserIcon className="h-4 w-4 mt-0.5 text-primary-foreground flex-shrink-0" />
                      )}
                      <div className="flex-1">
                        <div className="whitespace-pre-wrap text-sm">
                          {message.content}
                        </div>
                        <div className="text-xs opacity-70 mt-2">
                          {message.timestamp.toLocaleTimeString('pt-BR', { 
                            hour: '2-digit', 
                            minute: '2-digit' 
                          })}
                        </div>
                      </div>
                    </div>
                  </div>
                </div>
              ))}
              
              {isProcessing && (
                <div className="flex justify-start">
                  <div className="bg-accent text-accent-foreground p-4 rounded-lg">
                    <div className="flex items-center gap-2">
                      <CpuChipIcon className="h-4 w-4 text-primary" />
                      <div className="flex space-x-1">
                        <div className="w-2 h-2 bg-primary rounded-full animate-bounce"></div>
                        <div className="w-2 h-2 bg-primary rounded-full animate-bounce" style={{animationDelay: '0.1s'}}></div>
                        <div className="w-2 h-2 bg-primary rounded-full animate-bounce" style={{animationDelay: '0.2s'}}></div>
                      </div>
                    </div>
                  </div>
                </div>
              )}
              
              <div ref={messagesEndRef} />
            </div>
          </ScrollArea>

          {/* Quick Commands */}
          <div className="px-6 py-2 border-t border-border">
            <div className="flex flex-wrap gap-2">
              {quickCommands.map((command, index) => (
                <Button
                  key={index}
                  variant="outline"
                  size="sm"
                  onClick={() => setInputMessage(command.text)}
                  className="text-xs"
                >
                  <command.icon className="h-3 w-3 mr-1" />
                  {command.text}
                </Button>
              ))}
            </div>
          </div>

          {/* Input Area */}
          <div className="p-6 border-t border-border">
            <div className="flex gap-2">
              <div className="flex gap-1">
                <Button
                  size="icon"
                  variant="outline"
                  onClick={simulateVoiceInput}
                  title="Simular entrada de voz"
                >
                  <MicrophoneIcon className="h-4 w-4" />
                </Button>
                <Button
                  size="icon"
                  variant="outline"
                  onClick={simulateImageInput}
                  title="Simular OCR de imagem"
                >
                  <PhotoIcon className="h-4 w-4" />
                </Button>
              </div>
              
              <Input
                value={inputMessage}
                onChange={(e) => setInputMessage(e.target.value)}
                onKeyPress={handleKeyPress}
                placeholder="Digite sua mensagem... (Ex: 'Gastei R$ 50 no supermercado')"
                className="flex-1"
                disabled={isProcessing}
              />
              
              <Button
                onClick={handleSendMessage}
                disabled={!inputMessage.trim() || isProcessing}
                className="bg-gradient-primary"
              >
                <PaperAirplaneIcon className="h-4 w-4" />
              </Button>
            </div>
            
            <p className="text-xs text-muted-foreground mt-2">
              üí° Experimente: "Gastei R$ 50 no supermercado", "Qual meu saldo?", "Recebi R$ 200"
            </p>
          </div>
        </CardContent>
      </Card>
    </div>
  );
}